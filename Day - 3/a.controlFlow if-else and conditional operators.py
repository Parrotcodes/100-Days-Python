#ControlFlow if-else and conditional operators

print('Below are the basic examples for the controlFlow and conditional operations')

#Example - 1
waterlevel = 50

if waterlevel >= 80:
    print('Drain Water from the BathTub')
else:
    print('Continue filling the BathTub')

#Example - 2
height = int(input('Enter the children height to check eligibilty to ride:'))

if height >= 120:
    print('You can ride the rollercoaster')
else:
    print('You are not eligible to ride the rollercoaster')

#Example - 3
isRainfall = False

if isRainfall:
    print('You should carry the umbrella')
else:
    print('You can have fun outside!')
    
